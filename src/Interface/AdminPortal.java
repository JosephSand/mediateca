    public void GenerarIdentificador ()throws SQLException{
    
     statement =Conexion.getConnection().prepareStatement("SELECT IFNULL((SELECT CONCAT ('DVD' , CAST( (SELECT  LPAD((SELECT (MAX(Codigo) + 1) FROM `material_dvd` LIMIT 1),5,'0') FROM `material_dvd` LIMIT 1) AS CHAR(8)))),'DVD00001')");
     ResultSet Lector = statement.executeQuery();
     
     while (Lector.next()) {
         
         jTextIdentificadorDVD.setText(Lector.getString(1));    
        
     }
     } 
    
    
